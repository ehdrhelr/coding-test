package chapter11_practice;

import java.util.*;

public class AdventurerGuild1 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int n = sc.nextInt();
        int[] arr = new int[n];
        for (int i = 0; i < n; i++) {
            arr[i] = sc.nextInt();
        }
        Arrays.sort(arr);
        System.out.println(Arrays.toString(arr));
        int groups = 0;
        int people = 0;
        for (int i = 0; i < arr.length; i++) {
            people += 1;
            if (arr[i] > people) {
                continue;
            }
            groups += 1;
            people = 0;
        }
        System.out.println(groups);
    }
}

